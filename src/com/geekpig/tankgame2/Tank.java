package com.geekpig.tankgame2;

public class Tank {
    private int x;//the x coordinate of tank
    private int y;//the y coordinate of tank

    public Tank(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }
}
